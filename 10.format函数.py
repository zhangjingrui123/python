#format 字符串格式化函数
#基本语法是通过{}和：代替以前的%
print("{:.2f}".format(3.1415926))  #保留小数点后2位
print("{:+.2f}".format(3.1415926)) #带符号保留小数点后2位
print("{:+.3f}".format(-3.1415926))#带符号保留小数点后2位
print("{:.0f}".format(3.1415926))  #不保留小数位
print("{:,}".format(1000000))      #以逗号分隔数字格式
print("{:.2%}".format(0.25))       #以百分比格式显示
